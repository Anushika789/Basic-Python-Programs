#Write a program to print day according to the day number entered by the user.
day_num=int(input("enter the day number (1-monday to 7-sunday)"))
if (day_num==1):
    print("monday")
if(day_num==2):
    print("tuesday")
if(day_num==3):
    print("wednesday")
if(day_num==4):
    print("thursday")
if(day_num==5):
    print("friday")
if(day_num==6):
    print("saturday")
if(day_num==7):
    print("sunday")

