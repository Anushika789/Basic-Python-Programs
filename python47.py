#Write a program to print color name, if user enters the first letter of the color name.
color=input("enter the first letter of color name:")
if(color=="r" or color=="R"):
    print("red")
if(color=="b" or color=="B"):
    print("black")
if(color=="p" or color=="P"):
    print("pink")
if(color=="w" or color=="W"):
    print("white")
if(color=="o" or color=="O"):
    print("orange")
if(color=="y" or color=="Y"):
    print("yellow")
if(color=="G" or color=="g"):
    print("green")
