#write the program to find perimeter of circle rectangle triangle
r=float(input("enter the radius of circle:"))
pi=3.14
perimeter=2*pi*r
print("the perimeter of circle is",perimeter)

h=float(input("enter the height of rectangle:"))
b=float(input("enter the breadth of rectangle:"))
perimeter1=2*(h+b)
print("the perimeter of rectangle is",perimeter1)

a1=float(input("enter the first side:"))
a2=float(input("enter the second side:"))
a3=float(input("enter the third side:"))
perimeter2=a1+a2+a3
print("the perimeter of triangle is",perimeter2)
